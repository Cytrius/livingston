        // stop previous requests (if any)
        if (this.request) {
            this.request.abort();
        }

        // start ajax request

        // debug("----DATA");
        // debug(sendData);

        this.request = jQuery.ajax({
            url: "http://livingstonintl.com/livingston-content/plugins/leadgen/leadgen-submit-proxy.php",
            type: "POST",
            data: sendData,
            dataType: "json"
        });

        // receive ajax request results

        this.request.done(function (receivedData, textStatus, jqXHR){
            debug("AJAX DONE:");
            debug(receivedData);
            if (receivedData['status'] == "success") {
                // display request success
                LeadGenForm.showSubmitResult(true);
            } else {
                // display request error
                LeadGenForm.showSubmitResult(false, receivedData);
            }
        });

        this.request.fail(function (jqXHR, textStatus, errorThrown){
            // debug("AJAX FAIL:");
            // debug(receivedData);
            // display request error
            LeadGenForm.showSubmitResult(false);
